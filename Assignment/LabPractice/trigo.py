import math

print("sine function")
print(round(math.sin(math.radians(0)), 2))
print(round(math.sin(math.radians(30)), 2))
print(round(math.sin(math.radians(45)), 2))
print(round(math.sin(math.radians(60)), 2))
print(round(math.sin(math.radians(90)), 2))

print("cosine function")
print(round(math.cos(math.radians(0)), 2))
print(round(math.cos(math.radians(30)), 2))
print(round(math.cos(math.radians(45)), 2))
print(round(math.cos(math.radians(60)), 2))
print(round(math.cos(math.radians(90)), 2))

print("tangent function")
print(round(math.tan(math.radians(0)), 2))
print(round(math.tan(math.radians(30)), 2))
print(round(math.tan(math.radians(45)), 2))
print(round(math.tan(math.radians(60)), 2))
print(round(math.tan(math.radians(90)), 2))
